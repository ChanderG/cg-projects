/*
 * Contains data structures used in the projects.
 */
#ifndef DS_H
#define DS_H

/* Point structure
 * Holds x,y,z coordinates of a point in 3D.
 */
typedef struct {
	int x;
	int y;
	int z;
} Point;

/* 2d Point structure
 * Holds x,y coordinates of a point in 2D.
 */
typedef struct {
	int x;
	int y;
} Point2D;

#endif
